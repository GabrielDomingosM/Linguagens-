# 2) Faça um script que leia um número e mostre o seu dobro, seu triplo e a sua raiz quadrada

n1 = int(input("Digite um número: "))
dobro = n1*2
print(f"{dobro} é o dobro do seu número")
triplo = n1*3
print(f"{triplo} é o triplo do seu número")
